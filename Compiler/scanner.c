void InsertInTokenList  (int intTokenCode,char *strTokenSourceCodeText,int intRow,int intColumn){
    t_token *ptrAux;
    t_token *ptrNewToken =  (t_token*)malloc(sizeof (t_token));
    ptrNewToken->intTokenCode=intTokenCode;
    ptrNewToken->strTokenSourceCodeText=(char *)malloc(strlen(strTokenSourceCodeText)+1);
    ptrNewToken->strTokenSourceCodeText[0]='\0';
    strcpy(ptrNewToken->strTokenSourceCodeText,strTokenSourceCodeText);
    ptrNewToken->intRow=intRow;
    ptrNewToken->intColumn=intColumn;
    ptrNewToken->ptrNext=NULL;

    if (ptrTokenList==NULL){
        ptrTokenList=ptrNewToken;
    }else{
        ptrAux=ptrTokenList;
        while (ptrAux->ptrNext!=NULL){
            ptrAux=ptrAux->ptrNext;
        }
        ptrAux->ptrNext=ptrNewToken;
    }
    //printf ("\nAÃ±adiendo %f %",intTokenCode,strTokenSourceCodeText,intRow,intColumn);
}

