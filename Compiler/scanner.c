int scanner (char * strFileName){
    FILE *ptrSourceCODE;
    ptrTokenList=NULL;
    int intCurrentState=0;
    int intNextState;
    int intInputSymbolCode;
    int intNodeType;
    int intTerminalSymbolCode;
    char c;
    int boolReadFlag;
    char *strTerminalSymbol;
    char *strBuffer=(char *)malloc(1024);
    strBuffer [0]='\0';
    int intLenAux;
    int intRow=1;
    int intColumn=0;
    int intTokenColumn;
    int intTokenRow;
    ptrSourceCODE= fopen(strFileName,"r");

    if (ptrSourceCODE==NULL){
        perror ("Error in source code chi\n");
    }else{
        do{

            if (boolReadFlag){
                c=getc (ptrSourceCODE);

                if (c=='\n'){
                    intRow++;
                    intColumn=0;
                }else{

                    if (c=='\t'){
                        intColumn=intColumn+(SPACES_PER_TAB-(intColumn % SPACES_PER_TAB));
                    }
                    else
                    intColumn++;
                }
            }

            if (intCurrentState==0){
                intTokenRow=intRow;
                intTokenColumn=intColumn;
            }

            if (!boolReadFlag){
                boolReadFlag=TRUE;
            }
           //printf ("%c\n",c);
            intInputSymbolCode=  GetInputSymbolCode (c);
            intNextState = TM [intCurrentState][intInputSymbolCode];
            intNodeType = TM [intCurrentState][DFA_NODE_TYPE];

            if ((intNextState==RECOGNIZED_TOKEN) //something is recognized
                && ((intNodeType == NODE_TYPE_Final)
                || (intNodeType == NODE_TYPE_IntermediateFinal))){
                 //RECOGNIZED
                intTerminalSymbolCode = TM [intCurrentState][DFA_OUTPUT_CODE];
                if (!IsSeparatorToken (intTerminalSymbolCode))
                {
                    strTerminalSymbol  = GetText_TerminalSymbol (intTerminalSymbolCode);
                    printf ("Recognized Token r[%d]c[%d]- Code[%d]-Desc:[%s]-Buffer:[%s]\n",
                            intTokenRow,intTokenColumn,intTerminalSymbolCode,strTerminalSymbol,strBuffer);
            InsertInTokenList(intTerminalSymbolCode,strBuffer,intTokenRow,intTokenColumn);
                    free (strTerminalSymbol); //later this instructtion will change
                }
                intCurrentState=0;
                boolReadFlag=FALSE;
                strBuffer[0]='\0';
            }else if (intNextState==SCANNER_ERROR){ //lrxical error
                //ELSE RETURN "NO"
                printf  ("Lexical Errorr[%d]c[%d]-Buffer:[%s]\n",
                        intTokenRow,intTokenColumn,strBuffer);
            }else{
            //traversing DFA in current Token
                intCurrentState=intNextState;
                intLenAux =strlen(strBuffer);
                strBuffer[intLenAux]=c;
                strBuffer[intLenAux+1]='\0';
            }
        } while(c!=EOF);
        InsertInTokenList(TERMINAL_SYMBOL_EOF,"TERMINAL_SYMBOL_EOF",intTokenRow,intTokenColumn);
        fclose(ptrSourceCODE);
        free(strBuffer);
    }
    return 0;
}//scanner

